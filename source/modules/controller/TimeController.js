class TimeController
{
  // ##########################################################################
  // PUBLIC MEMBERS
  // ##########################################################################

  // ==========================================================================
  // Constructor
  // ==========================================================================

  constructor(main)
  {
    this._main = main

    // ------------------------------------------------------------------------
    // Member Variables
    // ------------------------------------------------------------------------

    this._minYear = 1970
    this._maxYear = 1999
  }


  // ==========================================================================
  // Setter
  // ==========================================================================

  setMinYear(year)
  {
    // TODO: consistency check:
    // - really a year?
    // - smaller than max year?
    this._minYear = year
  }

  setMaxYear(year)
  {
    // TODO: consistency check:
    // - really a year?
    // - larger than min year?
    this._maxYear = year
  }


  // ==========================================================================
  // Getter
  // ==========================================================================

  getMinYear()
  {
    return this._minYear
  }

  getMaxYear()
  {
    return this._maxYear
  }

  getNumYears()
  {
    return this._maxYear - this._minYear
  }


  // ##########################################################################
  // PRIVATE MEMBERS
  // ##########################################################################

}
