class Helpers
{
  // ##########################################################################
  // PUBLIC MEMBERS
  // ##########################################################################

  // ==========================================================================
  // Constructor
  // ==========================================================================

  constructor(main)
  {
    this._main = main
  }

  // ==========================================================================
  // Mixin two objects (override A -> default B)
  // ==========================================================================

  mixin(objA, objB)
  {
    let objMixin = this.deepCopy(objB)
    for (var key in objA)
      objMixin[key] = objA[key]
    return objMixin
  }

  // ==========================================================================
  // Deep copy of an object
  // ==========================================================================

  deepCopy(obj)
  {
    return JSON.parse(JSON.stringify(obj));
  }


}
